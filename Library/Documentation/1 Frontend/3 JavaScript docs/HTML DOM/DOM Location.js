// https://www.w3schools.com/jsref/obj_location.asp
// The location object contains information about the current URL.
// The location object is part of the window object and is accessed through the window.location property.

// Location Object Properties

// Property // Description // URL
location.hash; // Sets or returns the anchor part (#) of a URL // https://www.w3schools.com/jsref/prop_loc_hash.asp
location.host; // Sets or returns the hostname and port number of a URL // https://www.w3schools.com/jsref/prop_loc_host.asp
location.hostname; // Sets or returns the hostname of a URL // https://www.w3schools.com/jsref/prop_loc_hostname.asp
location.href; // Sets or returns the entire URL // https://www.w3schools.com/jsref/prop_loc_href.asp
location.origin; // Returns the protocol, hostname and port number of a URL // https://www.w3schools.com/jsref/prop_loc_origin.asp
location.pathname; // Sets or returns the path name of a URL // https://www.w3schools.com/jsref/prop_loc_pathname.asp
location.port; // Sets or returns the port number of a URL // https://www.w3schools.com/jsref/prop_loc_port.asp
location.protocol; // Sets or returns the protocol of a URL // https://www.w3schools.com/jsref/prop_loc_protocol.asp
location.search; // Sets or returns the querystring part of a URL // https://www.w3schools.com/jsref/prop_loc_search.asp

Method; // Description
location.assign(); // Loads a new document // location.assign("https://www.w3schools.com") // https://www.w3schools.com/jsref/met_loc_assign.asp
location.reload(); // Reloads the current document // https://www.w3schools.com/jsref/met_loc_reload.asp
location.replace(); // Replaces the current document with a new one // https://www.w3schools.com/jsref/met_loc_replace.asp