// The Console object provides access to the browser's debugging console.

// Method; // Description // URL
console.assert(); // Writes an error message to the console if the assertion is false // https://www.w3schools.com/jsref/met_console_assert.asp
console.clear(); // Clears the console // https://www.w3schools.com/jsref/met_console_clear.asp
console.count(); // Logs the number of times that this particular call to count() has been     called // https://www.w3schools.com/jsref/met_console_count.asp
console.error(); // Outputs an error message to the console // https://www.w3schools.com/jsref/met_console_error.asp
console.group(); // Creates a new inline group in the console. This indents following     console messages by an additional level, until console.groupEnd() is called // https://www.w3schools.com/jsref/met_console_group.asp
console.groupCollapsed(); // Creates a new inline group in the console. However, the new group is     created collapsed. The user will need to use the disclosure button to expand     it // https://www.w3schools.com/jsref/met_console_groupcollapsed.asp
console.groupEnd(); // Exits the current inline group in the console // https://www.w3schools.com/jsref/met_console_groupend.asp
console.info(); // Outputs an informational message to the console // https://www.w3schools.com/jsref/met_console_info.asp
console.log(); // Outputs a message to the console // https://www.w3schools.com/jsref/met_console_log.asp
console.table(); // Displays tabular data as a table // https://www.w3schools.com/jsref/met_console_table.asp
console.time(); // Starts a timer (can track how long an operation takes) // https://www.w3schools.com/jsref/met_console_time.asp
console.timeEnd(); // Stops a timer that was previously started by console.time() // https://www.w3schools.com/jsref/met_console_timeend.asp
console.trace(); // Outputs a stack trace to the console // https://www.w3schools.com/jsref/met_console_trace.asp
console.warn(); // Outputs a warning message to the console // https://www.w3schools.com/jsref/met_console_warn.asp
