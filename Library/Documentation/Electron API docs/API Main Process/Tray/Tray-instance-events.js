//www.electronjs.org/docs/api/tray#instance-events
// The Tray module emits the following events:

'click'; //Emitted when the tray icon is clicked.
'right-click'; //Emitted when the tray icon is right clicked.
'double-click'; //Emitted when the tray icon is double clicked.
'balloon-show'; // Emitted when the tray balloon shows.
'balloon-click'; // Emitted when the tray balloon is clicked.
'balloon-closed'; // Emitted when the tray balloon is closed because of timeout or user manually closes it.
'drop'; // Emitted when any dragged items are dropped on the tray icon.
'drop-files'; // Emitted when dragged files are dropped in the tray icon.
'drop-text'; // Emitted when dragged text is dropped in the tray icon.
'drag-enter'; // Emitted when a drag operation enters the tray icon.
'drag-leave'; // Emitted when a drag operation exits the tray icon.
'drag-end'; // Emitted when a drag operation ends on the tray or ends at another location.
'mouse-up'; // Emitted when the mouse is released from clicking the tray icon.
'mouse-down'; //Emitted when the mouse clicks the tray icon.
'mouse-enter'; // Emitted when the mouse enters the tray icon.
'mouse-leave'; // Emitted when the mouse exits the tray icon.
'mouse-move'; // Emitted when the mouse moves in the tray icon.
